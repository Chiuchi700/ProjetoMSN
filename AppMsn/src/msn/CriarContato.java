    //Cadastrando com o dados recebidos
    protected void cadastrar() {
        
        if (((nome.getText().length() > 2 && nome.getText().length() < 21) && num.getText().length() != 11)
                && num.getText().replace(" ", "").length() == 14) {
            
            if (WhatsApp.conversaExiste(num.getText()) == false) {
                JOptionPane.showMessageDialog(null, "Erro, contato ja existe na sua lista", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            } else if (WhatsApp.getNumero().equals(num.getText())) {
                JOptionPane.showMessageDialog(null, "Erro, você não pode colocar a si mesmo na lista de contatos", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            } else {
                WhatsApp.inicarConversa(num.getText(), nome.getText());
            }
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "Erro, preencha os campos corretamente \n Nome entre 3 e 20 caracteres", "Erro", JOptionPane.ERROR_MESSAGE);
            nome.setText("");
            num.setText("");
        }
        
    }

