    //Atualizando a letra na 'foto' de perfil
    private void nomeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nomeKeyPressed
        
        if (nome.getText().length() > 1) {
            char at = Character.toUpperCase(nome.getText().charAt(0));
            
            if (at > 64 && at < 91 || at > 96 && at < 123) {
                letra_user1.setText(at + "");
            }
        }
    }//GEN-LAST:event_nomeKeyPressed

